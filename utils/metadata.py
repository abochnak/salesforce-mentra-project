def substr_list_filter(items, txt):
    matches = []
    for i in items:
        if txt.lower() in i.lower():
            matches.append(i)
    return list(set(matches))

def substr_map_filter(items, txt):
    matches = []
    for k,v in items.items():
        if k.lower() in txt.lower():
            return [k]
        if type(v) == str:
            if v.lower() in txt.lower():
                matches.append(k)
                continue
        else:
            for i in v:
                if i.lower() in txt.lower():
                    matches.append(k)
                    continue
                if txt.lower() in i.lower():
                    matches.append(k)
    return list(set(matches))
